package Refeicao;

public class Nutrientes {
    
    private int codigoNutrientes;
    private String nomeNutrientes;

    public int getCodigoNutrientes() {
        return codigoNutrientes;
    }

    public void setCodigoNutrientes(int codigoNutrientes) {
        this.codigoNutrientes = codigoNutrientes;
    }

    public String getNomeNutrientes() {
        return nomeNutrientes;
    }

    public void setNomeNutrientes(String nomeNutrientes) {
        this.nomeNutrientes = nomeNutrientes;
    }
    
    
}
