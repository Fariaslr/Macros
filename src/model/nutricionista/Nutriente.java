package model.nutricionista;

public class Nutriente {

    private int codigoNutriente;
    private String nomeNutriente;

    public int getCodigoNutriente() {
        return codigoNutriente;
    }

    public void setCodigoNutrientes(int codigoNutrientes) {
        this.codigoNutriente = codigoNutrientes;
    }

    public String getNomeNutrientes() {
        return nomeNutriente;
    }

    public void setNomeNutrientes(String nomeNutrientes) {
        this.nomeNutriente = nomeNutrientes;
    }

}
